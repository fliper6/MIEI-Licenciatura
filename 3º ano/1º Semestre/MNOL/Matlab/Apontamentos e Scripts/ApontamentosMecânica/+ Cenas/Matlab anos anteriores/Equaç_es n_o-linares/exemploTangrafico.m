%Considere a equação f(x)= 1 -x tan(x) =0 para x ?0. 
% Localize graficamente as duas menores raízes positivas. 

x= linspace(0,5);
y1=1./x;
y2=tan(x);

figure(1)
hold on
axis([0  5 0 5]) 
plot(x,y1,'r', x, y2, 'b')
grid on

% podemos determinar as raízes com fzero
f=inline('1-x.*tan(x)');

display('Um intervalo que contém a menor raíz positiva é [0.5, 1]');
r1=fzero(f, [0.5 1])

display('Um intervalo que contém a segunda raíz positiva é [3, 3.5]');
r2=fzero(f, [3, 3.5])

% podemos agora incluir no gráfico as raízes eos pontos de intersecção

plot(r1, 0, 'or', r2, 0, 'or')
plot(r1, tan(r1), 'or', r2, tan(r2), 'or')





