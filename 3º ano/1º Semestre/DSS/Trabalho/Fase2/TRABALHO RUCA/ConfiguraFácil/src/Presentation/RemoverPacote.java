    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int indice = jl.getSelectedIndex();
        try{
            Facade.removerPacote(this.pacotes.get(indice).getNome());
            JOptionPane.showMessageDialog(null,"Pacote " + this.pacotes.get(indice).getNome() + " removido");
            }
            catch(Exception e){JOptionPane.showMessageDialog(null,e.getMessage());}
        if(pacotes.size()==1){
            new MenuPacotes().setVisible(true);
            this.dispose();
            return;
        }
        this.pacotes.remove(indice);
        this.dlm.remove(indice);
        this.jl=new JList(this.dlm);
        this.jScrollPane1.setViewportView(this.jl);
    }//GEN-LAST:event_jButton1ActionPerformed

