    public ConfiguracaoDetalhes(Configuracao c,ArrayList<String> comps){
        initComponents();
        this.c=c;
        this.comps=comps;
        DefaultListModel dlm = new DefaultListModel();
        dlm.addElement(c.toString());
        for(String s : comps)
            dlm.addElement(s);
        JList jl = new JList(dlm);
        this.jScrollPane1.setViewportView(jl);
    }
    
    public ConfiguracaoDetalhes(Configuracao c,ArrayList<String> comps,Cliente cliente,String passado,ArrayList<Cliente> clientes){
        initComponents();
        this.c=c;
        this.comps=comps;
        DefaultListModel dlm = new DefaultListModel();
        dlm.addElement(c.toString());
        for(String s : comps){
            //JOptionPane.showMessageDialog(null,s);
            dlm.addElement(s);
        }
        JList jl = new JList(dlm);
        this.jScrollPane1.setViewportView(jl);
        this.cliente=cliente;
        this.passado=passado;
        this.clientes=clientes;
    }

