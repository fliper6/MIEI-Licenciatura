    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
         Facade.configuracaoNaFila(this.c.getId());
        }
        catch(ConfiguracaoJaNaFilaException e){JOptionPane.showMessageDialog(null,e.getMessage()); return;}
        catch(Exception e){JOptionPane.showMessageDialog(null,e.getMessage()); return;}
        if(this.cliente==null)
            new PedidoPagamento(this.c,this.comps).setVisible(true);
        else new PedidoPagamento(this.c,this.comps,this.cliente,this.passado,this.clientes).setVisible(true);
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

