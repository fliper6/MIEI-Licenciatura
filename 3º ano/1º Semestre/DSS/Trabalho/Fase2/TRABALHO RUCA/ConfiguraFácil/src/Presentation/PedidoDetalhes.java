    public PedidoDetalhes(Configuracao c,ArrayList<String> comps,String passado){
        initComponents();
        this.c=c;
        this.comps=comps;
        this.passado=passado;
        DefaultListModel dlm = new DefaultListModel();
        dlm.addElement(c.toString());
        for(String s : comps)
            dlm.addElement(s);
        JList jl = new JList(dlm);
        this.jScrollPane1.setViewportView(jl);
        if(passado.equals("stand")){
            this.jButton1.setVisible(false);
            this.jButton2.setVisible(false);
            this.jButton3.setVisible(false);
        }
        
    }

