    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try{
            Facade.removerPedido(this.c.getId());
            JOptionPane.showMessageDialog(null,"Pedido removido");
            
        }
        catch(Exception e){JOptionPane.showMessageDialog(null,e.getMessage());}
        
        HashMap<Configuracao,ArrayList<String>> p=new HashMap<>();
        try{
            p=Facade.getPedidos();
        }
        catch(Exception e){JOptionPane.showMessageDialog(null,e.getMessage());}
        new FilaDeEspera(p,this.passado).setVisible(true);
        this.dispose();
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{
            Facade.verificaStock(this.comps);
            
            Facade.iniciarProducao(this.c.getId());
            
            JOptionPane.showMessageDialog(null,"Produção iniciada");
            HashMap<Configuracao,ArrayList<String>> p=new HashMap<>();
        try{
            p=Facade.getPedidos();
        }
        catch(Exception e){JOptionPane.showMessageDialog(null,e.getMessage());}
        new FilaDeEspera(p,this.passado).setVisible(true);
        this.dispose();
        }
        catch(NaoExistemUnidadesSuficientesException e){JOptionPane.showMessageDialog(null,e.getMessage());}
        catch(Exception e){JOptionPane.showMessageDialog(null,e.getMessage());}
// TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

