    public void testaDep() {
        try{
            if(Facade.existemDependencias(this.escolhidos)) {
                this.dependencias=true;
                HashMap<String,HashSet<String>> dependencias=Facade.listaDependencias(this.escolhidos);
                for(String s : dependencias.keySet()){
                    StringBuilder sb = new StringBuilder();
                    for(String s1 : dependencias.get(s))
                        sb.append(s1).append(" ");
                    JOptionPane.showMessageDialog(null,"" + s +" requer: " + sb.toString());
                }
            }
            else this.dependencias=false;
        }
        catch(Exception e){}
    }
    
    public boolean verificarObrigatorios() {
        ArrayList<String> emFalta = new ArrayList<String>();
        Boolean falta=true;
        String[] ob={"chassi","pintura","jantes","pneus","motor","banco","estofo","volante","TuboDeEscape","vidro","capo"};
        for(String s : ob){
            falta=true;
            for(Componente c : this.escolhidos)
                if(c.getNome().contains(s)){
                    falta=false;
                    break;
                }
            if(falta) emFalta.add(s);
        }
        if(!emFalta.isEmpty()){
            StringBuilder sb = new StringBuilder();
            for(String s : emFalta)
                sb.append(s).append(" ");
            JOptionPane.showMessageDialog(null,"Componentes obrigatórios em falta: " +sb.toString());
            return false;
        }
        return true;
    }
    
