    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int indice1=this.jl.getSelectedIndex();
        if(!escolhidos.contains(this.componentes.get(indice1))) {
            escolhidos.add(this.componentes.get(indice1));
            this.dlm2.addElement(this.componentes.get(indice1).toString2());
            this.jl2=new JList(this.dlm2);
            this.jScrollPane3.setViewportView(this.jl2);
        }
        try {
        if (Facade.existemIncompatibilidades(this.escolhidos)){
            this.incompatibilidades=true;
            jButton1.setVisible(false);
            jButton3.setVisible(false);
            jButton4.setVisible(false);
            jButton5.setVisible(false);
            try{
            HashSet<String> incompativeis = Facade.listaIncompatibilidades(this.escolhidos);
            StringBuilder sb = new StringBuilder();
            sb.append("Os seguinte componentes estão a gerar incompatibilidades:\n");
            for(String s : incompativeis)
                sb.append(s);
            JOptionPane.showMessageDialog(null,sb.toString());
                }
            catch(Exception e){
                JOptionPane.showMessageDialog(null,e.getMessage());
            }      
        }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        boolean adicionarPacote=true;
        for(Pacote p : this.pacotes.keySet())
            if(p.getNome().equals(this.jl1.getSelectedValue()))
                for(Componente c : this.pacotes.get(p)) {
                    if(!escolhidos.contains(c)){
                    this.escolhidos.add(c);
                    this.dlm2.addElement(c.toString2());
                    }
                    else
                        adicionarPacote=false;
                }
        if(adicionarPacote)
            for(Pacote p: this.pacotes.keySet())
                if(p.getNome().equals(this.jl1.getSelectedValue()))
                    this.pacotesEscolhidos.put(p,this.pacotes.get(p));
        this.jl2=new JList(this.dlm2);
        this.jScrollPane3.setViewportView(this.jl2);
        testaInc();
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int indice3=this.jl2.getSelectedIndex();
        for(Pacote p : this.pacotesEscolhidos.keySet())
            for(Componente c : this.pacotesEscolhidos.get(p))
                if(c.getNome().equals(this.escolhidos.get(indice3).getNome())){
                    this.pacotesEscolhidos.remove(p);   
                    break;
                }
        this.escolhidos.remove(indice3);
        this.dlm2.remove(indice3);
        this.jl2=new JList(this.dlm2);
        this.jScrollPane3.setViewportView(this.jl2);
            try {
        if (Facade.existemIncompatibilidades(this.escolhidos)){
            try{
            HashSet<String> incompativeis = Facade.listaIncompatibilidades(this.escolhidos);
            StringBuilder sb = new StringBuilder();
            sb.append("Os seguinte componentes estão a gerar incompatibilidades:\n");
            for(String s : incompativeis)
                sb.append(s);
            JOptionPane.showMessageDialog(null,sb.toString());
            this.incompatibilidades=true;
            this.jButton1.setVisible(false);
            this.jButton3.setVisible(false);
            this.jButton4.setVisible(false);
            this.jButton5.setVisible(false);
                }
            catch(Exception e){
                JOptionPane.showMessageDialog(null,e.getMessage());
            }      
        }
        else {
            this.incompatibilidades=false;
            this.jButton1.setVisible(true);
            this.jButton3.setVisible(true);
            this.jButton4.setVisible(true);
            this.jButton5.setVisible(true);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

